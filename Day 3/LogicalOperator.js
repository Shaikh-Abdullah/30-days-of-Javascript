// && ampersand operator example
const check1 = 4 > 2 && 8 > 6;
const check2 = 100 > 30 && 90 > 2;
console.log(check1, check2);

// || pipe or operator, example
const check = 3 < 6 || 6 > 9;
const check3 = 3 > 6 || 6 > 9;
console.log(check, check3);

// || pipe or operator, example
let checking = 4 > 3;
let checking2 = !(4 > 3);
let lightOn = true;
let lightOff = !lightOn;
let isMarried = !false;
console.log(checking, checking2, lightOn, lightOff, isMarried);

//////// Increment Operator ///////////
let count = 4;
console.log(count++);
console.log(count);

//Decrement Operator
console.log(count--);
console.log(count);
